case ("snowing")
	when "raining" 	then puts "Get an umbrella, a coat and rubber boots!"
	when "foggy"	then puts "wear a light coat"
	when "snowing" 	then puts "Better stay home!"
	when "sunny"	then puts "Hey, it's time for some shades, shorts, & sunblock!"	
	else puts "Be ready for unforeseen circumstances!"
end
